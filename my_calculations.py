
class Calculations:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def find_sum(self):
        return self.a + self.b

    def find_diff(self):
        return self.a - self.b

    def find_product(self):
        return self.a * self.b

    def find_quotient(self):
        return self.a / self.b

